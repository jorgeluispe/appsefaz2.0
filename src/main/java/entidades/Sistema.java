public class Sistema {
    private int codSis;
    private String descSis;

    /**
     * @return the codSis
     */
    public int getCodSis() {
        return codSis;
    }

    /**
     * @param codSis the codSis to set
     */
    public void setCodSis(int codSis) {
        this.codSis = codSis;
    }

    /**
     * @return the descSis
     */
    public String getDescSis() {
        return descSis;
    }

    /**
     * @param descSis the descSis to set
     */
    public void setDescSis(String descSis) {
        this.descSis = descSis;
    }
            
}
