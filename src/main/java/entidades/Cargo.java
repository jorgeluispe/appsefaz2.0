package entidades;

/**
 *
 * @author Jorge
 */
public class Cargo {
    private int codCar;
    private String descCar;

    /**
     * @return the codCar
     */
    public int getCodCar() {
        return codCar;
    }

    /**
     * @param codCar the codCar to set
     */
    public void setCodCar(int codCar) {
        this.codCar = codCar;
    }

    /**
     * @return the descCar
     */
    public String getDescCar() {
        return descCar;
    }

    /**
     * @param descCar the descCar to set
     */
    public void setDescCar(String descCar) {
        this.descCar = descCar;
    }
    
}
