public class UsuarioSistema {
    private int codUsu;
    private int codSis;

    /**
     * @return the codUsu
     */
    public int getCodUsu() {
        return codUsu;
    }

    /**
     * @param codUsu the codUsu to set
     */
    public void setCodUsu(int codUsu) {
        this.codUsu = codUsu;
    }

    /**
     * @return the codSis
     */
    public int getCodSis() {
        return codSis;
    }

    /**
     * @param codSis the codSis to set
     */
    public void setCodSis(int codSis) {
        this.codSis = codSis;
    }
            
}
