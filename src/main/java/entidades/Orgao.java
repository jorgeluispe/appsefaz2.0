public class Orgao {
    
    private int codOrg; 
    private String descOrg;

    /**
     * @return the codOrg
     */
    public int getCodOrg() {
        return codOrg;
    }

    /**
     * @param codOrg the codOrg to set
     */
    public void setCodOrg(int codOrg) {
        this.codOrg = codOrg;
    }

    /**
     * @return the descOrg
     */
    public String getDescOrg() {
        return descOrg;
    }

    /**
     * @param descOrg the descOrg to set
     */
    public void setDescOrg(String descOrg) {
        this.descOrg = descOrg;
    }
    
}
